#include "libft.h"

int	ft_atoi(const char *str)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	b = 1;
	c = 0;
	while ((str[a] > 8 && str[a] < 14) || str[a] == 32)
		a++;
	if (str[a] == 43 || str[a] == 45)
	{
		if (str[a] == 45)
			b = -1;
		a++;
	}
	while (str[a] > 47 && str[a] < 58)
	{
		c = c * 10 + (str[a] - 48);
		a++;
	}
	return (c * b);
}
